        "  - modellnummer  = the alphanumeric first token from the model line (e.g., 'CQ1616TA')\n"
        "  - artikelnummer = the 5-digit first token from the article line (e.g., '04599')\n"
        "  - menge         = 1\n"
        "  - furncloud_id  = '' (unless a Furnplan/Stückliste line is present)\n"
        "  - human_review_needed = true (non-standard layout, needs verification)\n"
        "  Example:\n"
        "    '1  Schwebetürenschrank  System One'    (right-aligned: 846416 — ignore)\n"
        "    'CQ1616TA Front und Korpus Artisan Eiche Dekor,'\n"
        "    'Leisten breit alufarbig, Innendekor Texline'\n"
        "    'Ausführung 2'\n"
        "    '04599 3trg. mit 1 Spiegeltüre'\n"
        "    -> modellnummer='CQ1616TA', artikelnummer='04599', menge=1\n"
        "\n"
        "--- FORMAT H — Space-separated 'MODELL ARTIKEL' standalone line ---\n"
        "  Trigger: A standalone line (not preceded by 'Ausf.', 'Art.', 'Typ', 'Ty.') whose\n"
        "  FIRST token is an alphanumeric code (contains both letters and digits) AND whose\n"
        "  SECOND token is exactly 5 digits, OR 5 digits followed by exactly 1 letter,\n"
        "  OR one of the letters A/B/C followed by exactly 5 digits,\n"
        "  with NO hyphen between them.\n"
        "  - modellnummer  = first token (the alphanumeric code)\n"
        "  - artikelnummer = second token (the 5-digit code)\n"
        "  - menge         = 1\n"
        "  - furncloud_id  = '' (empty)\n"
        "  Example:\n"
        "    'PDAW61SP96 57383'\n"
        "    -> modellnummer='PDAW61SP96', artikelnummer='57383', menge=1\n"
        "  Guard: The second token must be 5 or 6 chars (5 digits, or 5 digits + 1 letter).\n"
        "  Do NOT match lines where the second token is a 6-digit pure number.\n"
        "\n"
        "--- NO MATCHING FORMAT — artikelnummer and modellnummer empty ---\n"
        "  If none of the above formats (D, F, E, A, B, H, I) match for a position:\n"
        "  - artikelnummer = '' (empty string)\n"
        "  - modellnummer  = '' (empty string)\n"
        "  - menge         = 1 (Braun default — position number is NOT quantity)\n"
        "  - furncloud_id  = '' (empty)\n"
        "  - Create ONE output item.\n"
        "  Do NOT use the right-aligned Braun reference number as artikelnummer.\n"
        "  A 6-digit number (e.g., 879878, 885019) is NEVER a valid artikelnummer.\n"
        "  Setting artikelnummer='' will trigger post-processing to request the missing code.\n"
        "\n"
        "ACCESSORY LINES — which are separate items vs. attributes:\n"
        "  Lines starting with '[N]x [token]' fall into one of three categories:\n"
        "\n"
        "  1. SEPARATE ITEM — '[N]x Description CODE-CODE' (Format D3): contains a D-format\n"
