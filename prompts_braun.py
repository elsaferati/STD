        "  - artikelnummer = code after 'Typ:' or 'Ty.:' (may include letters, e.g., '70594G')\n"
        "  - menge         = 1 (Braun default — position number is NOT quantity)\n"
        "  - modellnummer  = empty string\n"
        "  - furncloud_id  = '' (empty)\n"
        "  - Create ONE output item.\n"
