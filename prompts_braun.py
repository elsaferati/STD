        "--- FORMAT B — Single item: 'Typ: NNNNN' or 'Ty.: NNNNN' (colon separator) ---\n"
        "  Trigger: A 'Typ: [CODE]' or 'Ty.: [CODE]' line (with colon) present,\n"
        "           no Art.Nr. line, AND no model code extractable from an Ausf. line.\n"
