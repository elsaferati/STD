        "  All G sub-cases:\n"
        "  - Create ONE additional output item per qualifying line.\n"
        "  - artikelnummer = the valid code found\n"
        "  - modellnummer  = '' (empty)\n"
        "  - menge         = 1 by default; = N if preceded by '[N]x' (e.g., '2x12736' → menge=2)\n"
